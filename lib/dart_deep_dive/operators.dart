// Operators in Dart are symbols used to perform operations on variables.
// Each operator defines how the operands (data values) will be processed to produce a result.

// Operands : It represents the data.
// Operator : It represents how the operands will be processed to produce a value.
// For example, in the expression 2 + 3, 2 and 3 are operands, and + is the operator.

