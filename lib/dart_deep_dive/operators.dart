/// ******************************************************************************
/// ***                   arithmetic operators                                         ***
/// ******************************************************************************

/// 1. **Addition (+):** This operator is used to add two operands together.
///
/// 2. **Subtraction (-):** This operator is used to subtract the second operand from the first operand.
///
/// 3. **Unary Minus (-expr):** The unary minus operator is used to reverse the sign of an expression. For example, `-5` would reverse the sign of `5`.
///
/// 4. **Multiplication (*):** This operator is used to multiply two operands.
///
/// 5. **Division (/):** This operator is used to divide the first operand by the second operand.
///
/// 6. **Integer Division (~/:** The integer division operator is used to divide the first operand by the second operand, but it returns the result as an integer, discarding any fractional part. For example, `5 ~/ 2` would result in `2`.
///
/// 7. **Modulus (%):** The modulus operator is used to find the remainder when the first operand is divided by the second operand. For example, `5 % 2` would result in `1`, as the remainder of dividing 5 by 2 is 1.
